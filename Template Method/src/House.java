public abstract class House {
    public void DoorType(){
        System.out.println("It has 1 normal door.");
    }

    public void WindowType(){
        System.out.println("It has 4 normal windows.");
    }

    public void RoofType(){
        System.out.println("It has 1 triangular roof.");
    }

    public void WallType(){
        System.out.println("It has 4 walls.");
    }
}
